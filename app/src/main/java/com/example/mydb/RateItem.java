package com.example.mydb;

public class RateItem {
    private String Name;
    private String Rate;
    private String Id;

    public void setName(String name) {
        Name = name;
    }

    public String getName() {
        return Name;
    }


    public void setRate(String rate) {
        Rate = rate;
    }

    public String getRate() {
        return Rate;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getId() {
        return Id;
    }
}
